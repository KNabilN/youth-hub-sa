
CREATE TABLE public.category_skills (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  category_id uuid NOT NULL REFERENCES public.categories(id) ON DELETE CASCADE,
  name text NOT NULL,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE(category_id, name)
);

ALTER TABLE public.category_skills ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Public read category skills" ON public.category_skills FOR SELECT USING (true);
CREATE POLICY "Admin manage category skills" ON public.category_skills FOR ALL TO authenticated USING (public.has_role(auth.uid(), 'super_admin'));

